package Oder;

public class Coffee {
	private String cname;
	private String size;
	private int pice;
	private int ea;
	
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public int getPice() {
		return pice;
	}
	public void setPice(int pice) {
		this.pice = pice;
	}
	public int getEa() {
		return ea;
	}
	public void setEa(int ea) {
		this.ea = ea;
	}
}